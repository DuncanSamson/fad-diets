    <h2 class="text-xl pt-8 pb-2 font-bold">How Is It Made? </h2>

    <p class="text-lg">Canola seeds are genetically modified rapeseed's to lower the erucic acid content.
        They are then cleaned and gently heated to be softened.
        Then a chemical solvent is added called hexane to extract the oil from the seeds.
        The oil is then heated to 120 degrees to remove the majority of the hexane.
        Then its mixed with acid to remove gums and waxes.
        The oil is mixed with natural clay or activated carbon, which absorbs its pigments.
        Then it's filtered to remove the clay.
        The oil is then steam-distilled and ready to be added to your food.</p>
    <h2 class="text-xl pt-8 pb-2 font-bold">What Affects Could It Have On Your Brain?</h2>
    <p class="text-lg">A study showed that mice who consumed canola oil compared to those who didn’t were less adept on remembering and
        solving mazes, suggesting that it could impact on your ability to learn.</p>
    <h2 class="text-xl pt-8 pb-2 font-bold">What Affects Could It Have On Your Body?</h2>
    <p class="text-lg">In the same study the mice who were fed a diet of canola oil, gained more weight than the mice that didn’t,
        suggesting that sustained use could make you fatter.</p>
    <h2 class="text-xl pt-8 pb-2 font-bold">Fun Facts</h2>
    <p class="text-lg">Originally produced by the Canadian government to support the second world war effort as the oil lubricated wet
        metal for ships.
        Rapeseed Oil, in its original form was toxic for humans, the rape seed has since been genetically modified to
        lower its erucic acid content to below 2%.</p>
