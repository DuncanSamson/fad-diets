    <x-about-text>
        <p>Since childhood nutrition has been an issue to overcome, from being banned from consuming products with E
            numbers as they caused behavioural issues, to adulthood, suffering from insomnia, anxiety, depression,
            obesity and joint and muscle issues. These are ailments that plague many of us, adults and children alike in
            our society today. And your diet might be to blame. </p>
        <p>Many people including myself (Jonathan) have seen drastic improvements in their physical and mental health
            through the adaptation of what are commonly referred to as “fad” diets, while also eliminating ingredients
            from their diets that are categorised as safe for human consumption.</p>
        <p>The aim of this website is to bring some of this information to light in as engaging, interactive and concise
            way. But mainly, to encourage people to think more consciously as to what they are putting in their body and
            brain and consider how what they eat might be having on their own individual health. </p>
        <p>While we all share loads in common, everyone is unique in many different ways, a health food for one person
            might cause an allergic reaction in another. This website has been created to help you think more about what
            foods do and don’t work for you, so that you can live as healthy a life as possible and potentially free
            yourself from many of the conditions that affect so many of us today, without the need for medication. </p>
