    <h1 class="text-7xl font-bold text-center mb-6 pb-3 mb-10 border-b border-b-emerald-200">The Lion Diet</h1>
    <h2 class="text-xl pt-8 pb-2 font-bold">Who Invented It?</h2>
    <p class="text-lg">An elimination diet where you eat only ruminant meat such as beef and lamb, drink only water, and
        use only salt. It's designed to identify food sensitivities and reset your body.</p>
    <h2 class="text-xl pt-8 pb-2 font-bold">What Affects Could It Have On Your Brain?</h2>
    <p class="text-lg">Many followers report improved mood, reduced anxiety, fewer depressive episodes, and better
        emotional stability after removing all plant-based foods, as well as anecdotal reports that it has resolved
        medical issues including Bi-Polar.</p>
    <h2 class="text-xl pt-8 pb-2 font-bold">What Affects Could It Have On Your Body?</h2>
    <p class="text-lg">I people following the Lion Diet have claimed reduced inflammation, clearer skin, relief from
        chronic pain, better digestion, and significant weight loss. </p>
    <h2 class="text-xl pt-8 pb-2 font-bold">Fun Facts</h2>
    <p class="text-lg">The Lion Diet includes only three things: ruminant meat, salt, and water. No spices, oils, or
        caffeine.
        It’s often followed for 30–90 days before slowly reintroducing foods to test reactions.
        Mikhaila Peterson has documented her journey online, helping thousands try it for themselves.
        Mikhaila named the diet the Lion diet as it most reflected the diet of Lions who eat mostly ruminant meat.</p>
